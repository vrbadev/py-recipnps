def _eval_poly(terms, v):
    """Evaluate a sparse polynomial given (coeff, ((var,pow),...)) terms."""
    total = 0.0
    for coeff, factors in terms:
        prod = float(coeff)
        for var_idx, power in factors:
            prod *= v[var_idx] ** power
        total += prod
    return total


def _permute_vars(v, perm):
    """Swap index pair in each of the four 3-element groups (a,c,b,d)."""
    out = v.copy()
    i, j = perm
    for base in (0, 3, 6, 9):
        out[base + i], out[base + j] = v[base + j], v[base + i]
    return out


