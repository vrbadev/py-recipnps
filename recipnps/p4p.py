def _check_and_normalize_inputs(p_w, p_i, eps=1e-12):
    if p_w.shape != (3, 4) or p_i.shape != (3, 4):
        raise ValueError("p_w and p_i must both be shape (3, 4)")
    if np.any(np.isnan(p_w)) or np.any(np.isnan(p_i)):
        raise ValueError("p_w and p_i cannot contain NaN")
    if np.any(np.isinf(p_w)) or np.any(np.isinf(p_i)):
        raise ValueError("p_w and p_i cannot contain Inf")

    rank = np.linalg.matrix_rank(p_w - p_w[:, [0]])
    if rank < 2:
        raise ValueError("world points must not be all collinear")

    norms = np.linalg.norm(p_i, axis=0)
    if np.any(norms < eps):
        raise ValueError("bearing vectors must be non-zero")
    return p_i / norms


def _model_key(model, decimals=8):
    rot = np.round(model.rotation, decimals=decimals).flatten()
    trans = np.round(model.translation, decimals=decimals).flatten()
    return tuple(np.concatenate((rot, trans)))


def _deduplicate_models(models, decimals=8):
    unique = {}
    for model in models:
        unique[_model_key(model, decimals=decimals)] = model
    return list(unique.values())


def _pose_from_depths(p_w, p_i, lambdas, reprojection_threshold, eps=1e-12):
    if np.any(lambdas <= eps):
        return None

    p_cam = p_i * lambdas[None, :]

    w_centroid = np.mean(p_w, axis=1)
    c_centroid = np.mean(p_cam, axis=1)
    q_w = p_w - w_centroid[:, None]
    q_c = p_cam - c_centroid[:, None]

    h = q_w @ q_c.T
    u, _, vt = np.linalg.svd(h)
    r = vt.T @ u.T
    if np.linalg.det(r) < 0.0:
        vt[2, :] *= -1.0
        r = vt.T @ u.T
    t = c_centroid - r @ w_centroid

    model = Model(rotation=r, translation=t)
    dists = model.reprojection_dists_of(p_w, p_i)
    if np.any(np.isnan(dists)) or np.any(np.isinf(dists)):
        return None
    if np.max(dists) > reprojection_threshold:
        return None
    return model


def _quat_to_rotation(q):
    q = q / np.linalg.norm(q)
    q0, q1, q2, q3 = q
    return np.array(
        [
            [1.0 - 2.0 * (q2 * q2 + q3 * q3), 2.0 * (q1 * q2 - q0 * q3), 2.0 * (q1 * q3 + q0 * q2)],
            [2.0 * (q1 * q2 + q0 * q3), 1.0 - 2.0 * (q1 * q1 + q3 * q3), 2.0 * (q2 * q3 - q0 * q1)],
            [2.0 * (q1 * q3 - q0 * q2), 2.0 * (q2 * q3 + q0 * q1), 1.0 - 2.0 * (q1 * q1 + q2 * q2)],
        ],
        dtype=np.float64,
    )


def _dominant_quaternion_from_davenport(b, eps=1e-12):
    sigma = float(np.trace(b))
    s = b + b.T
    z = np.array([
        b[1, 2] - b[2, 1],
        b[2, 0] - b[0, 2],
        b[0, 1] - b[1, 0],
    ], dtype=np.float64)

    k = np.array(
        [
            [sigma, z[0], z[1], z[2]],
            [z[0], s[0, 0] - sigma, s[0, 1], s[0, 2]],
            [z[1], s[1, 0], s[1, 1] - sigma, s[1, 2]],
            [z[2], s[2, 0], s[2, 1], s[2, 2] - sigma],
        ],
        dtype=np.float64,
    )

    coeffs = np.poly(k)
    roots = np.roots(coeffs)
    real_roots = np.real(roots[np.abs(np.imag(roots)) < 1e-9])
    if real_roots.size == 0:
        return None
    lambda_max = float(np.max(real_roots))

    m = k - lambda_max * np.eye(4)
    q = None
    best_norm = 0.0
    for row in range(4):
        cofactors = np.zeros(4, dtype=np.float64)
        for col in range(4):
            sub = np.delete(np.delete(m, row, axis=0), col, axis=1)
            cofactors[col] = ((-1.0) ** (row + col)) * float(np.linalg.det(sub))
        nrm = float(np.linalg.norm(cofactors))
        if nrm > best_norm:
            best_norm = nrm
            q = cofactors

    if q is None or best_norm < eps:
        vals, vecs = np.linalg.eigh(k)
        idx = int(np.argmax(vals))
        q = np.real(vecs[:, idx])
        best_norm = float(np.linalg.norm(q))
        if best_norm < eps:
            return None

    q = q / best_norm
    if q[0] < 0.0:
        q = -q
    return q


def _pose_from_depths_algebraic(p_w, p_i, lambdas, reprojection_threshold, eps=1e-12):
    if np.any(lambdas <= eps):
        return None

    p_cam = p_i * lambdas[None, :]

    w_centroid = np.mean(p_w, axis=1)
    c_centroid = np.mean(p_cam, axis=1)
    q_w = p_w - w_centroid[:, None]
    q_c = p_cam - c_centroid[:, None]

    b = q_w @ q_c.T
    q = _dominant_quaternion_from_davenport(b, eps=eps)
    if q is None:
        return None

    r = _quat_to_rotation(q)
    t = c_centroid - r @ w_centroid

    model = Model(rotation=r, translation=t)
    dists = model.reprojection_dists_of(p_w, p_i)
    if np.any(np.isnan(dists)) or np.any(np.isinf(dists)):
        return None
    if np.max(dists) > reprojection_threshold:
        return None
    return model


